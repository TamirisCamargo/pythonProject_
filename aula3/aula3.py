"""
str - string
"""

# Ctrl+D - duplica linha.

print('alguma coisa')
print("Aspas duplas")
print('123456')

print("Essa é uma 'string' (str).")
print('Essa é uma "string" (str).')
print("Essa é uma \"string\" (str).")
print('Essa é uma \'string\' (str).')
print('Essa é uma \n (str).')  # /n quebra a linha
print(r'Essa é uma \n (str).')  # o r fala para o interpretador do Python que está mandando um texto e nada que está dentro é para ser executado.
print(r'Esse é meu \'Aspas aqui\' (str).')
